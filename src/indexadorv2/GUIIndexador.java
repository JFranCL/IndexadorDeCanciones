    private void btnCarpetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarpetaActionPerformed
        this.jFileChooser1.setVisible(true);
        this.jFileChooser1.setFileSelectionMode(JFileChooser.FILES_AND_DIRECTORIES);

        FileNameExtensionFilter imgFilter = new FileNameExtensionFilter("Mp3", "mp3");
        this.jFileChooser1.setFileFilter(imgFilter);
        int result = this.jFileChooser1.showOpenDialog(this);

        if (result != JFileChooser.CANCEL_OPTION) {

            File fileName = this.jFileChooser1.getSelectedFile();

            if ((fileName == null) || (fileName.getName().equals(""))) {
                this.ruta.setText("...");
            } else {
                this.ruta.setText(fileName.getAbsolutePath());
            }

        }
    }//GEN-LAST:event_btnCarpetaActionPerformed

    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        if(!this.rutaGuardadoM3u.getText().equals("") &&
                !this.txNombreM3u.getText().equals("introduce un nombre") &&
                !this.txNombreM3u.getText().equals("")){
            
            String contenidoM3u;
            String ruta = this.rutaGuardadoM3u.getText() + "\\";

            String archivo = this.txNombreM3u.getText() + ".m3u";
            File f = new File(ruta + archivo);  
            BufferedWriter bw = null;

            if (f.exists()) {
                int confirmado = JOptionPane.showConfirmDialog(this, "Vas a sobreescribir el archivo");

                if (JOptionPane.OK_OPTION == confirmado) {
                    try {
                        bw = new BufferedWriter(new FileWriter(f));
                        contenidoM3u = "#EXTM3U\n\n";
                        contenidoM3u = creaContenidoM3u(contenidoM3u);
                        bw.write(contenidoM3u);
                        bw.close();
                        this.rutaGuardadoM3u.setText("");
                        this.txNombreM3u.setText("");
                        ModelListaReproduccion.clear();

                    } catch (IOException ex) {
                        Logger.getLogger(GUIIndexador.class.getName()).log(Level.SEVERE, null, ex);
                    }

                }

            } else {
                JOptionPane.showMessageDialog(this, "Se ha generado un archivo nuevo");
                try {
                    bw = new BufferedWriter(new FileWriter(f));
                    contenidoM3u = "#EXTM3U\n\n";
                    contenidoM3u = creaContenidoM3u(contenidoM3u);
                    bw.write(contenidoM3u);
                    bw.close();
                    this.rutaGuardadoM3u.setText("");
                    this.txNombreM3u.setText("");
                    ModelListaReproduccion.clear();

                } catch (IOException ex) {
                    Logger.getLogger(GUIIndexador.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }else{
            JOptionPane.showMessageDialog(this, "Rellena los campos antes de continuar");
        }
    }//GEN-LAST:event_btnGenerarActionPerformed

    /**
     * Crea el texto necesario para configurar el archivo m3u con las canciones
     * de la lista de repodrucciÃ³n
     * @param contenidoM3u
     * @return 
     */
    private String creaContenidoM3u(String contenidoM3u) {
        ListModel listaAGenerar = ListaR.getModel();
        Canciones c;
        
        //Recorremos la Lista de canciones que queremos incluir
        for (int i = 0; i < listaAGenerar.getSize(); i++) {
            //Partimos en 2 Strings a partir de " - "
            //[0] -> Titulo de la canciÃ³n 
            //[1] -> Artista de la canciÃ³n
            String[] partes = listaAGenerar.getElementAt(i).toString().split(" - ");
            
            //Recorremos nuestro ArrayList que contiene todas las canciones
            for (int j = 0; j < ListaCanciones.size(); j++) {
                c = ListaCanciones.get(j);
                
                //Si el Titulo y Artista de la canciÃ³n coincide con los almacenados en partes
                //Se introduce los datos necesarios de cada canciÃ³n
                if (c.getId().getTitulo().equals(partes[0])
                        && c.getId().getArtista().equals(partes[1])) {
                    contenidoM3u += " #EXTINF:" + c.getDuracion()
                            + "," + partes[0]
                            + "\n"
                            + c.getRuta()
                            + "\n\n";

                }
            }
        }

        return contenidoM3u;
    }

    /**
     * Actualiza el comboBox de los Generos para poder seleccionar los disponibles
     */
    private void actualizaGeneros(Escaner e){

        ArrayList generos = e.localizaGeneros(ListaCanciones);
        
        for (int i = 0; i < generos.size(); i++) {
            this.comboGenero.addItem((String) generos.get(i));
        }
        
        //Cerramos la sesion en hibernate
        e.cierraSesion();
    }

    /**
     * Actualiza la lista de las canciones actualizadas 
     * @param e 
     */
    private void actualizaLocalizadas(Escaner e){
        ModelLocalizadas = new DefaultListModel();
        ListaCanciones = e.recuperaCanciones();
        Canciones c;

        for (int i = 0; i < ListaCanciones.size(); i++) {
            c = (Canciones) ListaCanciones.get(i);
            ModelLocalizadas.addElement(String.format("%s - %s", 
                    c.getId().getTitulo(), c.getId().getArtista()));
            ArchivosEncontrados.setModel(ModelLocalizadas);
        }
        
        actualizaGeneros(e);
    }
    
